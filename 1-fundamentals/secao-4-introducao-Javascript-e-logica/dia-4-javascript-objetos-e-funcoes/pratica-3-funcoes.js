function somar(a, b){
  return console.log(a + b);
}
somar(30, 40);


function subtrair(a, b){
  return console.log(a - b);
}
subtrair(100, 24);


function multiplicar(a, b){
  return console.log(a * b);
}
multiplicar(40, 8);


